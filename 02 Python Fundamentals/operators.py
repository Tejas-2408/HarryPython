## Arithmetic Operators
a = 23
b = 2

print("a+b = ",a+b)
print("a-b = ",a-b)
print("a*b = ",a*b)
print("a/b = ",a/b)
print("a%b = ",a%b)
print("a//b = ",a//b)
print("a**b = ",a**b)


## Conditional operator
print(a>4)
print(a<4)
print(a<=4)
print(a>=4)
print(a==4)
print(a!=4)


## Logical Operators
print(True or False)
print(True and False)
print(not True)


